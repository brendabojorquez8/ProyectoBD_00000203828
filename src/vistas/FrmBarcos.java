    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (this.txtId.getText().equals("0")) {
            if (!(this.txtMatricula.getText().isEmpty())
                    && !(this.txtNombre.getText().isEmpty()) && this.cmbSocios.getSelectedIndex() != 0
                    && !(this.txtCuota.getText().isEmpty()) && !(this.txtNumAmarre.getText().isEmpty())) {
                this.guardarBarco();
                this.limpiarFormulario();
            }else{
                JOptionPane.showMessageDialog(this, "Deben llenarse todos los datos", "Alerta", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            this.actualizarBarco();
            this.limpiarFormulario();
        }

        
        this.cargarBarcos();

    }//GEN-LAST:event_btnGuardarActionPerformed

