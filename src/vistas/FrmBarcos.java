    private void cargarBarcos() {
        List<Barco> barcos = this.control.obtenerBarcos(txtBusqueda.getText());
        if (barcos != null) {
            DefaultTableModel modelo = (DefaultTableModel) tblBarcos.getModel();
            //borra los registros
            modelo.setRowCount(0);
            for (Barco barco : barcos) {
                modelo.addRow(barco.toArray());
            }
        }

    }

    private void cargarCmb() {
        Socio seleccione = new Socio();
        seleccione.setNombre("Seleccione");
        cmbSocios.addItem(seleccione);
        List<Socio> socios = this.control.obtenerSocios(txtBusqueda.getText());
        if (socios != null) {
            for (Socio socio : socios) {
                cmbSocios.addItem(socio);
            }
        }
    }

    private void guardarBarco() {
        Barco barco = new Barco();
        barco.setNombre(txtNombre.getText());
        barco.setCuota(Float.valueOf(txtCuota.getText()));
        barco.setMatricula(txtMatricula.getText());
        barco.setNumAmarre(Integer.parseInt(txtNumAmarre.getText()));
        barco.setSocio((Socio) cmbSocios.getSelectedItem());
        this.control.guardarBarco(barco);
    }

    private void limpiarFormulario() {
        txtId.setText("0");
        txtNombre.setText(" ");
        txtCuota.setText("");
        txtNumAmarre.setText("");
        txtMatricula.setText("");
        this.cmbSocios.setSelectedIndex(0);

    }

    private void actualizarBarco() {
        Barco barco = new Barco();
        barco.setId(Integer.parseInt(txtId.getText()));
        barco.setNombre(txtNombre.getText());
        barco.setCuota(Float.valueOf(txtCuota.getText()));
        barco.setMatricula(txtMatricula.getText());
        barco.setNumAmarre(Integer.parseInt(txtNumAmarre.getText()));
        barco.setSocio((Socio) cmbSocios.getSelectedItem());
        this.control.actualizarBarco(barco);
    }

    private void cargarBarco() {
        int fila = this.tblBarcos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un barco", "información", JOptionPane.INFORMATION_MESSAGE);
        } else {
            DefaultTableModel modelo = (DefaultTableModel) this.tblBarcos.getModel();
            Integer idBarco = (Integer) modelo.getValueAt(fila, 0);
            Barco barco = this.control.consultarBarcoPorId(idBarco);
            if (barco != null) {
                txtId.setText(barco.getId().toString());
                txtNombre.setText(barco.getNombre());
                txtMatricula.setText(barco.getMatricula());
                txtCuota.setText(barco.getCuota().toString());
                txtNumAmarre.setText(barco.getNumAmarre().toString());
                cmbSocios.setSelectedItem(barco.getSocio());
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró al barco", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    private void eliminarBarco() {
        int fila = this.tblBarcos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un barco", "información", JOptionPane.INFORMATION_MESSAGE);
        } else {
            DefaultTableModel modelo = (DefaultTableModel) this.tblBarcos.getModel();
            Integer idBarco = (Integer) modelo.getValueAt(fila, 0);
            boolean eliminacionExitosa = this.control.eliminarBarco(idBarco);
            if (eliminacionExitosa) {
                JOptionPane.showMessageDialog(this, "Barco eliminado correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar al barco", "Informacion", JOptionPane.ERROR_MESSAGE);

            }
        }

    }

